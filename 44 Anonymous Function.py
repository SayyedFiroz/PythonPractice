# Python Lambda Functions are anonymous function means that the function is without a name.
# lambda keyword is used to define an anonymous function in Python.

# It has only one expression and f is object

# addition using lamda function

f = lambda a,b : a+b

result = f(5,4)
print(result)

# squaring using lambda function

s = lambda a :a*a
